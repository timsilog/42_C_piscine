char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = ((str[i] - 64 + 42) % 26) + 64;
		else if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = ((str[i] - 96 + 42) % 26) + 96;
		i++;
	}
	return (str);
}
