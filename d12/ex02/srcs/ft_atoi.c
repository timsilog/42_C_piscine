#include "tail.h"

int		ft_atoi(char *str)
{
	int num;
	int count;

	count = 0;
	num = 0;
	if (str[0] == '-' || str[0] == '+')
		count++;
	while (str[count] != '\0')
	{
		num *= 10;
		num += (str[count] - 48);
		count++;
	}
	return (num);
}
