#include "bsq.h"

int		is_num(char c)
{
	if (c >= '0' && c <= '9')
		return (1);
	return (0);
}

int		is_ch(char c, int ch[5])
{
	if (c == ch[2] || c == ch[3] || c == '\n' || '\0')
		return (1);
	return (0);
}
