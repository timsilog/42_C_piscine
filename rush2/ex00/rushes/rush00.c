#include "rush0.h"

void	do_rush00(int x, int y)
{
	if (y < 0 || x < 0)
		return ;
	if (y > 0)
	{
		printlines(x, 'o', '-', 'o');
		ft_putchar('\n');
		y--;
	}
	while (y > 1)
	{
		printlines(x, '|', ' ', '|');
		ft_putchar('\n');
		y--;
	}
	if (y == 1)
	{
		printlines(x, 'o', '-', 'o');
		ft_putchar('\n');
	}
}
