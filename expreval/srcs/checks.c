#include "eval.h"

 int     is_num(char nb)
{
	if (nb >= '0' && nb <= '9')
		return (1);
	return (0);
}

int     is_op(char c)
{
	if (c == '+' || c == '-' || c == '*'
			|| c == '/' || c == '%' || c == '(')
		return (1);
	return (0);
}

int     has_prec(char a, char on_stack)
{
	if (on_stack == '(' || a == '(')
		return (0);
	if ((a == '*' || a == '/' | a == '%') && (on_stack == '+' || on_stack == '-'))
		return (0);
	return (1);
}
