int		ft_iterative_factorial(int nb)
{
	int i;

	i = 1;
	if (nb > 16 || nb < 0)
	{
		return (0);
	}
	if (nb == 1 || nb == 0)
	{
		return (1);
	}
	while (nb > 0)
	{
		i *= nb;
		nb--;
	}
	return (i);
}
