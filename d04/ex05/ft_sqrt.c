int		ft_sqrt(int nb)
{
	int guess;

	guess = 0;
	if (nb > 2147395600)
	{
		return (0);
	}
	while (guess * guess < nb)
	{
		guess++;
	}
	if (guess * guess == nb)
	{
		return (guess);
	}
	else
	{
		return (0);
	}
}
