int		can_print(char n)
{
	if (n >= ' ' && n <= '~')
		return (1);
	return (0);
}

int		ft_str_is_printable(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!can_print(str[i]))
			return (0);
		i++;
	}
	return (1);
}
