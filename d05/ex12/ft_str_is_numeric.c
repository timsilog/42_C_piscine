int		is_number(char n)
{
	if (n >= 48 && n <= 57)
		return (1);
	return (0);
}

int		ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!is_number(str[i]))
			return (0);
		i++;
	}
	return (1);
}
